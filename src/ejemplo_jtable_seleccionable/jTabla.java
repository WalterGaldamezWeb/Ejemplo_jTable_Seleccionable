    //Metodo para llenar los jTextBox con los datos de la tabla
    public void llenar(){
        //Guardamos el valor de la fila seleccionada
        int fila = this.jTable1.getSelectedRow();
        //El numero de la columna aumentara segun corresponda al jTextBox
        int columna = 0;
        //Asignamos al jTextBox la celda y hacemos un parseo a string con el metodo valueOf()
        // y el metodo getValueAt()
        this.jTextId_producto.setText(String.valueOf(this.jTable1.getValueAt(fila, columna++)));
        this.jTextNombre_producto.setText(String.valueOf(this.jTable1.getValueAt(fila, columna++)));
        this.jTextPrecio_producto.setText(String.valueOf(this.jTable1.getValueAt(fila, columna++)));
        this.jTextCantidad.setText(String.valueOf(this.jTable1.getValueAt(fila, columna)));
    }
    
