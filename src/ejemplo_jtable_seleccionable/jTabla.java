    public jTabla() {
        initComponents();
        //Titulo del Jframe
        setTitle("Tabla Producto");
        //Centrar el jFrame en pantalla
        setLocationRelativeTo(null);
        
        
        Producto p = new Producto();
        p.setId_producto(25);
        p.setNombre_producto("Bebida Enlatada");
        p.setPrecio_Producto(0.50);
        p.setCantidad_producto(150);
        
        Producto p2 = new Producto();
        p2.setId_producto(35);
        p2.setNombre_producto("Galletas");
        p2.setPrecio_Producto(0.70);
        p2.setCantidad_producto(60);
        
        //Definimos los encabezados de las columnas
        String[] columnas = {"Id Producto","Nombre Producto","Precio Producto","Cantidad"};
        
        //Creamos el modela de la tabla le pasamos como parametro las columnas
        DefaultTableModel modeloTabla = new DefaultTableModel(null,columnas);
        
        //Definimos un arreglo tipo object sera la fila y cada indice una celda
        Object [] fila = new Object[4];
        fila[0] = p.getId_producto();
        fila[1] = p.getNombre_producto();
        fila[2] = p.getPrecio_Producto();
        fila[3] = p.getCantidad_producto();
        
        //Agregamos la fila al modelo con el metodo addRow
        modeloTabla.addRow(fila);
        
        Object [] fila2 = new Object[4];
        fila2[0] = p2.getId_producto();
        fila2[1] = p2.getNombre_producto();
        fila2[2] = p2.getPrecio_Producto();
        fila2[3] = p2.getCantidad_producto();
        
        modeloTabla.addRow(fila2);
        
        //Pasamos el modelo al Jtable con el metodo setModel para que lo muestre
        jTable1.setModel(modeloTabla);
        
       
    }
